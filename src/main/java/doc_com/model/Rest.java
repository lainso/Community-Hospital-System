package doc_com.model;

public class Rest {
	private int rid;
	private int did;
	private String rreason;
	private String rstart;
	private String rend;
	private int rcondition;
	public int getRid() {
		return rid;
	}
	public void setRid(int rid) {
		this.rid = rid;
	}
	public int getDid() {
		return did;
	}
	public void setDid(int did) {
		this.did = did;
	}
	public String getRreason() {
		return rreason;
	}
	public void setRreason(String rreason) {
		this.rreason = rreason;
	}
	public String getRstart() {
		return rstart;
	}
	public void setRstart(String rstart) {
		this.rstart = rstart;
	}
	public String getRend() {
		return rend;
	}
	public void setRend(String rend) {
		this.rend = rend;
	}
	public int getRcondition() {
		return rcondition;
	}
	public void setRcondition(int rcondition) {
		this.rcondition = rcondition;
	}

}
